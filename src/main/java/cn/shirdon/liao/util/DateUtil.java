    public static Date addDate(Date day , int type , int step){   
        Calendar cal = Calendar.getInstance();   
        cal.setTime(day);   
        if (type == 1) {		//小时
            cal.add(Calendar.HOUR, step);	
		} else if (type == 2) { //分钟
	        cal.add(Calendar.MILLISECOND, step);	
		} else if (type == 3){	//天
			cal.add(Calendar.DATE, step);	// 24小时制
		} else if (type == 4) {	//月
			cal.add(Calendar.MONTH, step);
		} else if (type == 5) {
			cal.add(Calendar.YEAR, step);
		} else {
			cal.add(Calendar.DATE, step);
		}
        return cal.getTime();
    }
    
